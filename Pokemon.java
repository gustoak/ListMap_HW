package HomeWorkMap;

public class Pokemon {
    private String namePokemon;
    private String typePokemon;
    private String categoryPokemon;

    public String getNamePokemon() {
        return namePokemon;
    }

    public void setNamePokemon(String namePokemon) {
        this.namePokemon = namePokemon;
    }

    public String getTypePokemon() {
        return typePokemon;
    }

    public void setTypePokemon(String typePokemon) {
        this.typePokemon = typePokemon;
    }

    public String getCategoryPokemon() {
        return categoryPokemon;
    }

    public void setCategoryPokemon(String categoryPokemon) {
        this.categoryPokemon = categoryPokemon;
    }
}
